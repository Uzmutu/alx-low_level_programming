/**
 * to_999 - take care of great number
 * @c: variable tested
 *
 * Description: print number from 1000
 *
 * Return: return value
 */
void to_999(int c)
{
	if (c <= 9)
		_putchar((c % 10) + '0');
	else if (c > 9 && c <= 99)
	{
		_putchar((c / 10) + '0');
		_putchar((c % 10) + '0');
	}
	else if (c > 99 && c <= 999)
	{
		_putchar((c / 100) + '0');
		_putchar(((c % 100) / 10) + '0');
		_putchar((c % 10) + '0');
	}
	if (c > 999 && c <= 9999)
	{
		_putchar((c / 1000) + '0');
		_putchar(((c % 1000) / 100) + '0');
		_putchar((((c % 1000) % 100) / 10) + '0');
		_putchar((c % 10) + '0');
	}
	else if (c > 9999 && c <= 99999)
	{
		_putchar((c / 10000) + '0');
		_putchar(((c % 10000) / 1000) + '0');
		_putchar((((c % 10000) % 1000) / 100) + '0');
		_putchar(((((c % 10000) % 1000) % 100) / 10) + '0');
		_putchar((c % 10) + '0');
	}

}
